struct AppConfig {
    static let defaultFileExtensions = ["ts", "rs", "py", "json", "css", "scss", "less", "sass", "c", "cpp"]

    var language: String = "auto"
    var terminal: String = "auto"
    var nvimPath: String = "/opt/homebrew/bin/nvim"
    var remoteOpen: String = "tab"
    var defaultFileExtensions: [String] = Self.defaultFileExtensions
    var tmuxMode: String = "never"
    var tmuxSession: String = ""
}

final class ConfigStore {
    static let shared = ConfigStore()

    let configURL: URL

    private init() {
        configURL = FileManager.default
            .homeDirectoryForCurrentUser
            .appendingPathComponent(".config/open-in-nvim/config")
    }

    func load() -> AppConfig {
        var config = AppConfig()

        guard let content = try? String(contentsOf: configURL, encoding: .utf8) else {
            if let nvim = findExecutable("nvim") {
                config.nvimPath = nvim
            }
            return config
        }

        for rawLine in content.split(separator: "\n", omittingEmptySubsequences: false) {
            let line = rawLine.trimmingCharacters(in: .whitespaces)
            guard !line.isEmpty, !line.hasPrefix("#"), let equalsIndex = line.firstIndex(of: "=") else {
                continue
            }

            let key = String(line[..<equalsIndex])
            var value = String(line[line.index(after: equalsIndex)...])
            value = value.trimmingCharacters(in: .whitespaces)
            value = value.trimmingCharacters(in: CharacterSet(charactersIn: "\"'"))

            switch key {
            case "OPEN_IN_NVIM_LANGUAGE":
                config.language = value
            case "OPEN_IN_NVIM_TERMINAL":
                config.terminal = value
            case "OPEN_IN_NVIM_NVIM":
                config.nvimPath = value
            case "OPEN_IN_NVIM_REMOTE_OPEN":
                config.remoteOpen = value
            case "OPEN_IN_NVIM_DEFAULT_EXTENSIONS":
                config.defaultFileExtensions = Self.parseExtensions(value)
            case "OPEN_IN_NVIM_TMUX":
                config.tmuxMode = value
            case "OPEN_IN_NVIM_TMUX_SESSION":
                config.tmuxSession = value
            default:
                continue
            }
        }

        return config
    }

    func save(_ config: AppConfig) throws {
        try FileManager.default.createDirectory(
            at: configURL.deletingLastPathComponent(),
            withIntermediateDirectories: true
        )

        let lines = [
            "# Generated by Open In Nvim settings",
            "OPEN_IN_NVIM_LANGUAGE=\(shellQuote(config.language))",
            "OPEN_IN_NVIM_TERMINAL=\(shellQuote(config.terminal))",
            "OPEN_IN_NVIM_NVIM=\(shellQuote(config.nvimPath))",
            "OPEN_IN_NVIM_REMOTE_OPEN=\(shellQuote(config.remoteOpen))",
            "OPEN_IN_NVIM_DEFAULT_EXTENSIONS=\(shellQuote(config.defaultFileExtensions.joined(separator: " ")))",
            "OPEN_IN_NVIM_TMUX=\(shellQuote(config.tmuxMode))",
            "OPEN_IN_NVIM_TMUX_SESSION=\(shellQuote(config.tmuxSession))"
        ].compactMap { $0 }

        try (lines.joined(separator: "\n") + "\n").write(to: configURL, atomically: true, encoding: .utf8)
    }

    static func parseExtensions(_ value: String) -> [String] {
        let separators = CharacterSet(charactersIn: ",，;； \n\t")
        var seen = Set<String>()

        return value
            .components(separatedBy: separators)
            .map { $0.trimmingCharacters(in: .whitespacesAndNewlines) }
            .map { $0.hasPrefix(".") ? String($0.dropFirst()) : $0 }
            .map { $0.lowercased() }
            .filter { !$0.isEmpty }
            .filter { extensionName in
                if seen.contains(extensionName) {
                    return false
                }
                seen.insert(extensionName)
                return true
            }
    }

    private func findExecutable(_ name: String) -> String? {
        let paths = [
            "/opt/homebrew/bin/\(name)",
            "/usr/local/bin/\(name)",
            "/usr/bin/\(name)",
            "/bin/\(name)"
        ]

        return paths.first { FileManager.default.isExecutableFile(atPath: $0) }
    }

    private func shellQuote(_ value: String) -> String {
        "'" + value.replacingOccurrences(of: "'", with: "'\\''") + "'"
    }
}


    private let nvimPathField = NSTextField()
    private let remoteOpenPopup = NSPopUpButton()
    private let tmuxModePopup = NSPopUpButton()
    private let tmuxSessionField = NSTextField()
    private let extensionOptionsStack = NSStackView()
    private let newExtensionField = NSTextField()
    private let statusLabel = NSTextField(labelWithString: "")
    private let configStore = ConfigStore.shared
    private let runner: OpenInNvimRunner

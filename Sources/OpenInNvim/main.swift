            section(title: t("section.general"), content: generalForm),
            section(title: t("section.newInstance"), content: newInstanceForm),
            section(title: t("section.defaultOpen"), content: extensionForm),
            section(title: t("section.advanced"), content: advancedForm),
            statusSeparator,

final class OpenInNvimRunner {
    private let scriptPath: String

    init() {
        if let resourcePath = Bundle.main.resourcePath {
            self.scriptPath = (resourcePath as NSString).appendingPathComponent("open-in-nvim.sh")
        } else {
            self.scriptPath = "./open-in-nvim.sh"
        }
    }

    func open(_ urls: [URL], completion: ((String?) -> Void)? = nil) {
        guard !urls.isEmpty else { return }

        let process = Process()
        process.executableURL = URL(fileURLWithPath: "/bin/zsh")
        process.arguments = [scriptPath] + urls.map { $0.path }

        let errorPipe = Pipe()
        process.standardError = errorPipe

        process.terminationHandler = { process in
            let errorData = errorPipe.fileHandleForReading.readDataToEndOfFile()
            let errorText = String(data: errorData, encoding: .utf8)?
                .trimmingCharacters(in: .whitespacesAndNewlines)

            DispatchQueue.main.async {
                if process.terminationStatus == 0 {
                    completion?(nil)
                } else {
                    let language = ConfigStore.shared.load().language
                    let fallback = AppText.text("error.scriptNoMessage", language: language)
                    completion?(errorText?.isEmpty == false ? errorText : fallback)
                }
            }
        }

        do {
            try process.run()
        } catch {
            completion?(error.localizedDescription)
        }
    }
}

enum DefaultAppRegistration {
    static func registerDefaultHandlers(for extensions: [String], language: String) -> [String] {
        guard let bundleID = Bundle.main.bundleIdentifier else {
            return [AppText.text("error.bundleId", language: language)]
        }

        var failures: [String] = []

        for fileExtension in extensions {
            guard let type = UTType(filenameExtension: fileExtension) else {
                failures.append(String(format: AppText.text("error.unknownType", language: language), fileExtension))
                continue
            }

            let status = LSSetDefaultRoleHandlerForContentType(
                type.identifier as CFString,
                .all,
                bundleID as CFString
            )

            if status != noErr {
                failures.append(String(format: AppText.text("error.launchServices", language: language), fileExtension, status))
            }
        }

        return failures
    }
}

enum NvimPluginInstaller {
    static var pluginSpecURL: URL {
        FileManager.default
            .homeDirectoryForCurrentUser
            .appendingPathComponent(".config/nvim/lua/plugins/open-in-nvim.lua")
    }

    static func install() throws -> URL {
        let url = pluginSpecURL
        try FileManager.default.createDirectory(
            at: url.deletingLastPathComponent(),
            withIntermediateDirectories: true
        )
        try pluginSpec.write(to: url, atomically: true, encoding: .utf8)
        return url
    }

    private static let pluginSpec = """
    return {
      "pzehrel/open-in-nvim",
      lazy = false,
      config = function(plugin)
        vim.opt.runtimepath:prepend(plugin.dir .. "/nvim-plugin")
        require("open-in-nvim").setup()
      end,
    }
    """
}


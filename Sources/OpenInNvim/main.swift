        ])
        stack.orientation = .vertical
        stack.spacing = 16
        stack.alignment = .leading
        stack.translatesAutoresizingMaskIntoConstraints = false

        contentView.addSubview(stack)

        NSLayoutConstraint.activate([
            stack.leadingAnchor.constraint(equalTo: contentView.leadingAnchor, constant: 28),
            stack.trailingAnchor.constraint(equalTo: contentView.trailingAnchor, constant: -28),
            stack.topAnchor.constraint(equalTo: contentView.topAnchor, constant: 24),
            stack.bottomAnchor.constraint(lessThanOrEqualTo: contentView.bottomAnchor, constant: -24),
            statusLabel.widthAnchor.constraint(equalTo: stack.widthAnchor),
            buttonStack.widthAnchor.constraint(equalTo: stack.widthAnchor)
        ])
    }

    private func t(_ key: String) -> String {
        AppText.text(key, language: currentLanguage)
    }

    private func setPopup(_ popup: NSPopUpButton, options: [(key: String, value: String)], selectedValue: String) {
        popup.removeAllItems()
        popup.addItems(withTitles: options.map { t($0.key) })

        if let index = options.firstIndex(where: { $0.value == selectedValue }) {
            popup.selectItem(at: index)
        } else {
            popup.selectItem(at: 0)
        }
    }

    private func rebuildUI(with config: AppConfig) {
        window?.contentView?.subviews.forEach { $0.removeFromSuperview() }
        extensionButtons.removeAll()
        buildUI()
        applyConfig(config)
    }

    private func form(views: [[NSView]]) -> NSGridView {
        let grid = NSGridView(views: views)
        grid.rowSpacing = 10
        grid.columnSpacing = 14
        grid.column(at: 0).xPlacement = .trailing
        grid.column(at: 1).width = 470
        return grid
    }

    private func section(title: String, content: NSView) -> NSStackView {
        let titleLabel = NSTextField(labelWithString: title)
        titleLabel.font = .boldSystemFont(ofSize: 13)
        titleLabel.textColor = .labelColor

        let separator = NSBox()
        separator.boxType = .separator
        separator.translatesAutoresizingMaskIntoConstraints = false
        separator.heightAnchor.constraint(equalToConstant: 1).isActive = true

        let headingStack = NSStackView(views: [titleLabel, separator])
        headingStack.orientation = .horizontal
        headingStack.spacing = 10
        headingStack.alignment = .centerY
        headingStack.distribution = .fill
        separator.setContentHuggingPriority(.defaultLow, for: .horizontal)

        let stack = NSStackView(views: [headingStack, content])
        stack.orientation = .vertical
        stack.spacing = 10
        stack.alignment = .leading
        stack.translatesAutoresizingMaskIntoConstraints = false
        headingStack.widthAnchor.constraint(equalTo: stack.widthAnchor).isActive = true
        return stack
    }

    private func label(_ text: String) -> NSTextField {
        let field = NSTextField(labelWithString: text)
        field.textColor = .secondaryLabelColor
        return field
    }

    private func loadConfig() {
        applyConfig(configStore.load())
    }

    private func applyConfig(_ config: AppConfig) {
        currentLanguage = config.language
        setPopup(languagePopup, options: languageOptions, selectedValue: config.language)
        let knownIndex = terminalOptions.firstIndex { $0.value == config.terminal && $0.value != "custom" }

        if let knownIndex {
            terminalPopup.selectItem(at: knownIndex)
            customTerminalField.stringValue = ""
        } else {
            if let customIndex = terminalOptions.firstIndex(where: { $0.value == "custom" }) {
                terminalPopup.selectItem(at: customIndex)
            }
            customTerminalField.stringValue = config.terminal
        }

        nvimPathField.stringValue = config.nvimPath
        tmuxSessionField.stringValue = config.tmuxSession
        setExtensionOptions(selectedExtensions: config.defaultFileExtensions)
        if let remoteIndex = remoteOpenOptions.firstIndex(where: { $0.value == config.remoteOpen }) {
            remoteOpenPopup.selectItem(at: remoteIndex)
        } else {
            remoteOpenPopup.selectItem(at: 0)
        }
        if let tmuxIndex = tmuxModeOptions.firstIndex(where: { $0.value == config.tmuxMode }) {
            tmuxModePopup.selectItem(at: tmuxIndex)
        } else {
            tmuxModePopup.selectItem(at: 0)
        }
        terminalChanged()
        tmuxModeChanged()
        statusLabel.stringValue = String(format: t("status.configFile"), configStore.configURL.path)
    }

    @objc private func terminalChanged() {
        let isCustom = selectedTerminalValue() == "custom"
        customTerminalField.isEnabled = isCustom
        customTerminalField.alphaValue = isCustom ? 1 : 0.45
    }

    @objc private func languageChanged() {
        var config = currentConfig()
        currentLanguage = selectedLanguageValue()
        config.language = currentLanguage
        rebuildUI(with: config)
    }

    @objc private func tmuxModeChanged() {
        let enabled = selectedTmuxModeValue() != "never"
        tmuxSessionField.isEnabled = enabled
        tmuxSessionField.alphaValue = enabled ? 1 : 0.45
    }

    @objc private func saveConfig() {
        do {
            let config = currentConfig()
            try configStore.save(config)
            let failures = DefaultAppRegistration.registerDefaultHandlers(
                for: config.defaultFileExtensions,
                language: config.language
            )
            let extensionsText = config.defaultFileExtensions.map { ".\($0)" }.joined(separator: " ")
            if failures.isEmpty {
                statusLabel.stringValue = String(format: t("status.saved"), extensionsText)
            } else {
                statusLabel.stringValue = String(format: t("status.partialDefaultFailure"), failures.joined(separator: "; "))
            }
        } catch {
            showError(t("error.saveFailed"), error.localizedDescription)
        }
    }

    @objc private func installNvimPlugin() {
        do {
            let url = try NvimPluginInstaller.install()
            statusLabel.stringValue = String(format: t("status.pluginInstalled"), url.path)
        } catch {
            showError(t("error.installFailed"), error.localizedDescription)
        }
    }

    private func currentConfig() -> AppConfig {
        let selected = selectedTerminalValue()
        let terminal = selected == "custom"
            ? customTerminalField.stringValue.trimmingCharacters(in: .whitespacesAndNewlines)
            : selected

        return AppConfig(
            language: selectedLanguageValue(),
            terminal: terminal.isEmpty ? "auto" : terminal,
            nvimPath: nvimPathField.stringValue.trimmingCharacters(in: .whitespacesAndNewlines),
            remoteOpen: selectedRemoteOpenValue(),
            defaultFileExtensions: selectedFileExtensions(),
            tmuxMode: selectedTmuxModeValue(),
            tmuxSession: normalizedTmuxSession()
        )
    }

    @objc private func addExtension() {
        let parsed = ConfigStore.parseExtensions(newExtensionField.stringValue)
        guard !parsed.isEmpty else {
            statusLabel.stringValue = t("status.enterExtension")
            return
        }

        addExtensionOptions(parsed, checked: true)
        newExtensionField.stringValue = ""
        statusLabel.stringValue = String(
            format: t("status.addedExtensions"),
            parsed.map { ".\($0)" }.joined(separator: " ")
        )
    }

    private func selectedLanguageValue() -> String {
        let index = languagePopup.indexOfSelectedItem
        guard languageOptions.indices.contains(index) else { return "auto" }
        return languageOptions[index].value
    }

    private func selectedTerminalValue() -> String {
        let index = terminalPopup.indexOfSelectedItem
        guard terminalOptions.indices.contains(index) else { return "auto" }
        return terminalOptions[index].value
    }

    private func selectedRemoteOpenValue() -> String {
        let index = remoteOpenPopup.indexOfSelectedItem
        guard remoteOpenOptions.indices.contains(index) else { return "tab" }
        return remoteOpenOptions[index].value
    }

    private func selectedTmuxModeValue() -> String {
        let index = tmuxModePopup.indexOfSelectedItem
        guard tmuxModeOptions.indices.contains(index) else { return "never" }
        return tmuxModeOptions[index].value
    }

    private func normalizedTmuxSession() -> String {
        tmuxSessionField.stringValue.trimmingCharacters(in: .whitespacesAndNewlines)
    }

    private func selectedFileExtensions() -> [String] {
        extensionOptions.filter { extensionButtons[$0]?.state == .on }
    }

    private func setExtensionOptions(selectedExtensions: [String]) {
        extensionOptions = AppConfig.defaultFileExtensions
        extensionButtons.removeAll()

        let selected = Set(selectedExtensions)
        for extensionName in selectedExtensions where !extensionOptions.contains(extensionName) {
            extensionOptions.append(extensionName)
        }

        rebuildExtensionOptions(checkedExtensions: selected)
    }

    private func addExtensionOptions(_ extensionNames: [String], checked: Bool) {
        for extensionName in extensionNames where !extensionOptions.contains(extensionName) {
            extensionOptions.append(extensionName)
        }

        var selected = Set(selectedFileExtensions())
        if checked {
            selected.formUnion(extensionNames)
        }
        rebuildExtensionOptions(checkedExtensions: selected)
    }

    private func rebuildExtensionOptions(checkedExtensions: Set<String>) {
        extensionButtons.removeAll()

        for view in extensionOptionsStack.arrangedSubviews {
            extensionOptionsStack.removeArrangedSubview(view)
            view.removeFromSuperview()
        }

        var rowViews: [NSButton] = []
        for (index, extensionName) in extensionOptions.enumerated() {
            let button = NSButton(
                checkboxWithTitle: ".\(extensionName)",
                target: nil,
                action: nil
            )
            button.state = checkedExtensions.contains(extensionName) ? .on : .off
            button.widthAnchor.constraint(greaterThanOrEqualToConstant: 72).isActive = true
            extensionButtons[extensionName] = button
            rowViews.append(button)

            if rowViews.count == 5 || index == extensionOptions.count - 1 {
                let row = NSStackView(views: rowViews)
                row.orientation = .horizontal
                row.spacing = 8
                row.alignment = .centerY
                extensionOptionsStack.addArrangedSubview(row)
                rowViews.removeAll()
            }
        }
    }

    private func showError(_ title: String, _ message: String) {
        let alert = NSAlert()
        alert.messageText = title
        alert.informativeText = message
        alert.alertStyle = .warning
        alert.runModal()
        statusLabel.stringValue = message
    }
}

final class ServiceProvider: NSObject {
    private let runner: OpenInNvimRunner
    private let didStartOpening: () -> Void

    init(runner: OpenInNvimRunner, didStartOpening: @escaping () -> Void) {
        self.runner = runner
        self.didStartOpening = didStartOpening
    }

    @objc func openInNvim(_ pasteboard: NSPasteboard, userData: String?, error: AutoreleasingUnsafeMutablePointer<NSString?>) {
        let urls = Self.urls(from: pasteboard)

        if urls.isEmpty {
            error.pointee = "没有从 Finder 收到文件或文件夹路径。"
            return
        }

        didStartOpening()
        runner.open(urls) { error in
            if let error {
                let language = ConfigStore.shared.load().language
                NSApp.setActivationPolicy(.regular)
                NSApp.activate(ignoringOtherApps: true)
                let alert = NSAlert()
                alert.messageText = AppText.text("error.openFailed", language: language)
                alert.informativeText = error
                alert.alertStyle = .warning
                alert.runModal()
            }
            NSApp.terminate(nil)
        }
    }

    private static func urls(from pasteboard: NSPasteboard) -> [URL] {
        var urls: [URL] = []

        if let fileURLs = pasteboard.readObjects(forClasses: [NSURL.self], options: nil) as? [URL] {
            urls.append(contentsOf: fileURLs)
        }

        if urls.isEmpty, let filenames = pasteboard.propertyList(forType: .fileURL) as? [String] {
            urls.append(contentsOf: filenames.compactMap(URL.init(string:)))
        }

        if urls.isEmpty, let filenames = pasteboard.propertyList(forType: .init("NSFilenamesPboardType")) as? [String] {
            urls.append(contentsOf: filenames.map { URL(fileURLWithPath: $0) })
        }

        return Array(Set(urls)).sorted { $0.path < $1.path }
    }
}

final class AppDelegate: NSObject, NSApplicationDelegate {
    private let runner = OpenInNvimRunner()
    private var serviceProvider: ServiceProvider?
    private var settingsWindowController: SettingsWindowController?
    private var didHandleExternalOpen = false
    private var didShowSettings = false

    func applicationDidFinishLaunching(_ notification: Notification) {
        let provider = ServiceProvider(runner: runner) { [weak self] in
            self?.didHandleExternalOpen = true
        }
        serviceProvider = provider
        NSApp.servicesProvider = provider
        NSUpdateDynamicServices()
        showSettingsIfIdle()
    }

    func application(_ application: NSApplication, open urls: [URL]) {
        didHandleExternalOpen = true
        runner.open(urls) { error in
            if let error {
                let language = ConfigStore.shared.load().language
                self.showAppForAlert()
                let alert = NSAlert()
                alert.messageText = AppText.text("error.openFailed", language: language)
                alert.informativeText = error
                alert.alertStyle = .warning
                alert.runModal()
            }
            application.terminate(nil)
        }
    }

    func applicationDidBecomeActive(_ notification: Notification) {
        showSettingsIfIdle()
    }

    func applicationShouldHandleReopen(_ sender: NSApplication, hasVisibleWindows flag: Bool) -> Bool {
        if !flag {
            showSettings()
        }
        return true
    }

    func applicationShouldTerminateAfterLastWindowClosed(_ sender: NSApplication) -> Bool {
        true
    }

    private func showSettings() {
        didShowSettings = true

        if settingsWindowController == nil {
            settingsWindowController = SettingsWindowController(runner: runner)
        }

        NSApp.setActivationPolicy(.regular)
        NSApp.activate(ignoringOtherApps: true)
        settingsWindowController?.showWindow(nil)
    }

    private func showSettingsIfIdle() {
        DispatchQueue.main.asyncAfter(deadline: .now() + 0.8) { [weak self] in
            guard let self, !self.didHandleExternalOpen, !self.didShowSettings else { return }
            self.showSettings()
        }
    }

    private func showAppForAlert() {
        NSApp.setActivationPolicy(.regular)
        NSApp.activate(ignoringOtherApps: true)
    }
}

let app = NSApplication.shared
let delegate = AppDelegate()
app.delegate = delegate
app.run()

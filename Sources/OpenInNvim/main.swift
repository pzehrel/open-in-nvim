    private var extensionOptions: [String] = AppConfig.defaultFileExtensions
    private var extensionButtons: [String: NSButton] = [:]

    private let languageOptions: [(key: String, value: String)] = [
        ("option.language.auto", "auto"),
        ("option.language.zhHans", "zh-Hans"),
        ("option.language.en", "en")
    ]

    private let terminalOptions: [(key: String, value: String)] = [
        ("option.terminal.auto", "auto"),
        ("option.terminal.ghostty", "ghostty"),
        ("option.terminal.alacritty", "alacritty"),
        ("option.terminal.iterm", "iterm"),
        ("option.terminal.terminal", "terminal"),
        ("option.terminal.custom", "custom")

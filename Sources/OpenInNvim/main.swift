        terminalPopup.action = #selector(terminalChanged)
        setPopup(remoteOpenPopup, options: remoteOpenOptions, selectedValue: "tab")
        setPopup(tmuxModePopup, options: tmuxModeOptions, selectedValue: "never")
        tmuxModePopup.target = self
        tmuxModePopup.action = #selector(tmuxModeChanged)

        customTerminalField.placeholderString = t("placeholder.customTerminal")
        nvimPathField.placeholderString = "/opt/homebrew/bin/nvim"
        tmuxSessionField.placeholderString = t("placeholder.tmuxSession")
        newExtensionField.placeholderString = t("placeholder.extension")

        extensionOptionsStack.orientation = .vertical
        extensionOptionsStack.spacing = 8
        extensionOptionsStack.alignment = .leading

        let addExtensionButton = NSButton(title: t("button.add"), target: self, action: #selector(addExtension))
        addExtensionButton.bezelStyle = .rounded

        let addExtensionStack = NSStackView(views: [newExtensionField, addExtensionButton])
        addExtensionStack.orientation = .horizontal
        addExtensionStack.spacing = 8
        addExtensionStack.alignment = .centerY
        addExtensionStack.distribution = .fill
        newExtensionField.widthAnchor.constraint(equalToConstant: 180).isActive = true

        let extensionControl = NSStackView(views: [extensionOptionsStack, addExtensionStack])
        extensionControl.orientation = .vertical
        extensionControl.spacing = 10
        extensionControl.alignment = .leading

        statusLabel.textColor = .secondaryLabelColor
